package exercise_day1;

public class Ex1_3_PremitiveInitialization {
	public static void main(String[] args) {
		byte b=2;
		//short s=4;
		int i=3;
		long l=100;
		float f=10.0f;
		//double d=7;
		
		short a=(short)b;
		float k=(float)i;
		int z=(int)f;
		int x=(int)l;
		
		System.out.println(a);
		System.out.println(k);
		System.out.println(z);
		System.out.println(x);
	}
	

}
